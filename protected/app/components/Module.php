namespace app\components;
use app\models\AccessLog;
use app\models\Setting;

class Module extends \yii\base\Module
{
    /**
     * @inheritdoc
     */
    public function init()
    {
        $setting = Setting::getSetting();
        \Yii::$app->params = array_merge(\Yii::$app->params , $setting);
        parent::init(); // TODO: Change the autogenerated stub
    }

    public function afterAction($action, $result)
    {
        $module = $this->id;
        $controller = $action->controller->id;
        $action = $action->id;

        register_shutdown_function(
            function()use($module,$controller,$action)
            {
                $log = new AccessLog();
                $log->access_time = time();
                $log->method = call_user_func(
                    function($method)
                    {
                        $method = strtoupper($method);
                        $map = ['GET','POST','PUT','DELETE'];
                        if (in_array($method,$map))
                        {
                            return array_search($method,$map);
                        }
                        return -1;
                    }
                    ,$_SERVER['REQUEST_METHOD']);
                $log->module = $module;
                $log->controller = $controller;
                $log->action = $action;
                $log->from = $_SERVER['REMOTE_ADDR'];
                $log->request = json_encode($_GET);
                $log->save(false);
            }
        );
        return parent::afterAction($action, $result); // TODO: Change the autogenerated stub
    }
}