<script>
    jQuery('#js-submit').on(
        'click',
        function()
        {
            var name = $('#name').val();
            var phone = $('#phone').val();
            var qq = $('#qq').val();
            var message = $('#message').val();

            if (phone.length == 0 && qq.length == 0)
            {
                alert('请至少留下QQ号码、微信号或者手机号码,方便跟进你的情况','完善信息');
                return;
            }

            if (message.length == 0)
            {
                alert('请填写留言');
                return;
            }


            Api.apiExamSave()
                .setSuccessCallback(
                    function()
                    {
                        alert('提交成功');
                    }
                )
                .setPayload('name',name)
                .setPayload('phone',phone)
                .setPayload('qq',qq)
                .setPayload('message',message)
                .send();
            return false;
        }
    )
</script>

